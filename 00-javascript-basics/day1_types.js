// Day 1: Data Types & Variables
// In QA, we use 'let' for values that change and 'const' for things that don't.

const candidateName = "Your Name"; // String
let yearsExperience = 0;           // Number
const isLearningQA = true;         // Boolean

// QA Check: Log it to the console
console.log(`Candidate: ${candidateName} | Exp: ${yearsExperience} | Learning: ${isLearningQA}`);

// Task: Change the experience
yearsExperience = 1; 
console.log(`Updated Exp: ${yearsExperience}`);